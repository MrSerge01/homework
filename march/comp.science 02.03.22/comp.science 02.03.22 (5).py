a = int(input("Temperature: "))

if -30 <= a <= 0:
    print("It's cold outside.")
elif 1 <= a <= 12:
    print("It's cool outside.")
elif 13 <= a <= 24:
    print("It's warm outside.")
elif a >= 25:
    print("It's hot outside.")

